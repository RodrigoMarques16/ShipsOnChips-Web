package sonc.quad;

/**
 * An object with x and y coordinates, each with its own getter.
 */
public interface HasPoint {

	/**
	 * Get X coordinate of this moving object
	 * 
	 * @return x coordinate of this moving object
	 */
	double getX();

	/**
	 * Get Y coordinate of this moving object
	 * 
	 * @return y coordinate of this moving object
	 */
	double getY();

}
